var PredictorI18n = (function () {
  var LANG_KEY = "predictor_lang";
  var lang = "ru";

  function detect() {
    try {
      var saved = localStorage.getItem(LANG_KEY);
      if (saved && PREDICTOR_I18N[saved]) return saved;
    } catch (e) {}
    return "ru";
  }

  function init() {
    lang = detect();
    document.documentElement.lang = lang === "hy" ? "hy" : lang;
  }

  function getLang() {
    return lang;
  }

  function setLang(next) {
    if (!PREDICTOR_I18N[next]) return;
    lang = next;
    try {
      localStorage.setItem(LANG_KEY, lang);
    } catch (e) {}
    document.documentElement.lang = lang === "hy" ? "hy" : lang;
  }

  function t(key, vars) {
    var dict = PREDICTOR_I18N[lang] || PREDICTOR_I18N.ru;
    var value = dict[key];
    if (value == null) value = PREDICTOR_I18N.ru[key] || key;
    if (vars) {
      Object.keys(vars).forEach(function (k) {
        value = String(value).split("{" + k + "}").join(vars[k]);
      });
    }
    return value;
  }

  function teamName(teamId) {
    var dict = PREDICTOR_I18N[lang] || PREDICTOR_I18N.ru;
    if (dict.teams && dict.teams[teamId]) return dict.teams[teamId];
    if (PREDICTOR_I18N.ru.teams[teamId]) return PREDICTOR_I18N.ru.teams[teamId];
    return teamId;
  }

  return {
    init: init,
    getLang: getLang,
    setLang: setLang,
    t: t,
    teamName: teamName,
  };
})();
